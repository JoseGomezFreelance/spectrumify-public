static void draw_char(SDL_Renderer *ren, int x, int y, char ch,
                       SDL_Color col) {
    int idx = ch - 32;
    if (idx < 0 || idx > 95) return;
    SDL_SetRenderDrawColor(ren, col.r, col.g, col.b, col.a);
    for (int row = 0; row < 8; row++) {
        uint8_t bits = FONT_8X8[idx][row];
        for (int col_bit = 0; col_bit < 8; col_bit++) {
            if (bits & (0x80 >> col_bit)) {
                SDL_RenderDrawPoint(ren, x + col_bit, y + row);
            }
        }
    }
}

static void draw_text(SDL_Renderer *ren, int x, int y, const char *text,
                       SDL_Color col, int scale) {
    for (int i = 0; text[i]; i++) {
        if (scale <= 1) {
            draw_char(ren, x + i*8, y, text[i], col);
        } else {
            /* Escalado: dibujar cada pixel como un rect */
            int idx = text[i] - 32;
            if (idx < 0 || idx > 95) continue;
            SDL_SetRenderDrawColor(ren, col.r, col.g, col.b, col.a);
            for (int row = 0; row < 8; row++) {
                uint8_t bits = FONT_8X8[idx][row];
                for (int cb = 0; cb < 8; cb++) {
                    if (bits & (0x80 >> cb)) {
                        SDL_Rect r = {x + i*8*scale + cb*scale,
                                      y + row*scale, scale, scale};
                        SDL_RenderFillRect(ren, &r);
                    }
                }
            }
        }
    }
}

static void draw_separator(SDL_Renderer *ren, int y) {
    SDL_SetRenderDrawColor(ren, C_BORDER.r, C_BORDER.g, C_BORDER.b, 255);
    SDL_Rect r = {0, y, WIN_W, SEP_H};
    SDL_RenderFillRect(ren, &r);
}

static void draw_header(SDL_Renderer *ren) {
    draw_text(ren, MARGIN, 12, "SPECTRUMIFY v0.1", C_CYAN, 2);
    draw_text(ren, WIN_W - 13*8 - MARGIN, 18, "(c) 2026 JGF", C_GRAY, 1);
}

static void draw_controls(SDL_Renderer *ren) {
    int y = WIN_H - CONTROLS_H + 10;
    const char *items[] = {"[L]","OAD  ","[E]","XPORT  ","[M]","ODE  ",
                           "[S]","IZE  ","[+/-]"," ZOOM  ","[C]","OMPRESS  ",
                           "[Q]","UIT", NULL};
    int x = MARGIN;
    for (int i = 0; items[i]; i++) {
        SDL_Color c = (i % 2 == 0) ? C_GREEN : C_WHITE;
        draw_text(ren, x, y, items[i], c, 1);
        x += (int)strlen(items[i]) * 8;
    }
}

static void draw_preview(SDL_Renderer *ren, SDL_Texture *orig_tex, int ow, int oh,
                          SDL_Texture *quant_tex, int qw, int qh, float zoom) {
    if (!orig_tex && !quant_tex) {
        draw_text(ren, WIN_W/2 - 15*8, PREVIEW_Y + PREVIEW_H/2,
                  "Pulsa [L] para cargar", C_GRAY, 1);
        return;
    }

    int half_w = (WIN_W - MARGIN*3) / 2;
    int avail_h = PREVIEW_H - MARGIN*2 - 16;
    int img_y = PREVIEW_Y + 16;

    draw_text(ren, MARGIN, PREVIEW_Y + 2, "ORIGINAL", C_GRAY, 1);
    draw_text(ren, MARGIN*2 + half_w, PREVIEW_Y + 2, "PIXEL-ART", C_GREEN, 1);

    /* Original */
    if (orig_tex) {
        float sc = half_w/(float)ow;
        if (avail_h/(float)oh < sc) sc = avail_h/(float)oh;
        int nw = (int)(ow * sc), nh = (int)(oh * sc);
        SDL_Rect dst = {MARGIN + (half_w-nw)/2, img_y + (avail_h-nh)/2, nw, nh};
        SDL_RenderCopy(ren, orig_tex, NULL, &dst);
    }

    /* Pixel-art con zoom */
    if (quant_tex) {
        float sc = half_w/(float)qw;
        if (avail_h/(float)qh < sc) sc = avail_h/(float)qh;
        sc *= zoom;
        int nw = (int)(qw * sc), nh = (int)(qh * sc);
        SDL_Rect dst = {MARGIN*2 + half_w + (half_w-nw)/2,
                        img_y + (avail_h-nh)/2, nw, nh};
        /* Clip */
        SDL_Rect clip = {MARGIN*2 + half_w, img_y, half_w, avail_h};
        SDL_RenderSetClipRect(ren, &clip);
        SDL_RenderCopy(ren, quant_tex, NULL, &dst);
        SDL_RenderSetClipRect(ren, NULL);
    }
}

